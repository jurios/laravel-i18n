<?php

namespace Kodilab\LaravelI18n\Tests;

use Illuminate\Database\Schema\Blueprint;
use Illuminate\Encryption\Encrypter;
use Illuminate\Filesystem\Filesystem;
use Illuminate\Support\Facades\App;
use Illuminate\Support\Facades\Schema;
use Kodilab\LaravelI18n\Models\Locale;

abstract class TestCase extends \Orchestra\Testbench\TestCase
{
    protected $resource_path = __DIR__ . DIRECTORY_SEPARATOR . 'resources';

    /** @var Filesystem */
    protected $filesystem;

    protected function setUp(): void
    {
        parent::setUp(); // TODO: Change the autogenerated stub

        $this->filesystem = new Filesystem();

        $this->artisan('migrate')->run();

        $this->withFactories(dirname(dirname(__FILE__)) . DIRECTORY_SEPARATOR . 'database/factories');

        $this->app['config']->set('app.key', 'base64:'.base64_encode(
            Encrypter::generateKey($this->app['config']->get('app.cipher'))
        ));

        $this->createResourcesPath();
    }

    protected function tearDown(): void
    {
        $this->removeResourcesPath();
        parent::tearDown(); // TODO: Change the autogenerated stub
    }

    protected function getPackageProviders($app)
    {
        return [
            \Kodilab\LaravelI18n\I18nProvider::class
        ];
    }

    /**
     * Define environment setup.
     *
     * @param  \Illuminate\Foundation\Application  $app
     * @return void
     */
    protected function getEnvironmentSetUp($app)
    {
        $config = include 'config/config.php';

        $app['config']->set('i18n', $config);
    }

    private function createResourcesPath()
    {
        if (file_exists($this->resource_path)) {
            $this->removeResourcesPath();
        }

        $this->filesystem->makeDirectory($this->resource_path, 0755, true);
    }

    private function removeResourcesPath()
    {
        $this->filesystem->deleteDirectory($this->resource_path);
    }
}
